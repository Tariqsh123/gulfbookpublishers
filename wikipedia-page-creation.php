    const individualsBtn = document.getElementById('individualsBtn');
    const companiesBtn = document.getElementById('companiesBtn');
    const individualsImages = document.getElementById('individualsImages');
    const companiesImages = document.getElementById('companiesImages');

    // Toggle Groups
    individualsBtn.addEventListener('click', () => {
        individualsImages.classList.remove('d-none');
        companiesImages.classList.add('d-none');
        individualsBtn.classList.add('active');
        companiesBtn.classList.remove('active');
    });

    companiesBtn.addEventListener('click', () => {
        companiesImages.classList.remove('d-none');
        individualsImages.classList.add('d-none');
        companiesBtn.classList.add('active');
        individualsBtn.classList.remove('active');
    });

    // Overlay functionality
    const overlay = document.getElementById('overlay');
    const overlayImg = document.getElementById('overlayImg');
    const closeBtn = document.querySelector('.close-btn');

    document.querySelectorAll('.image-wrapper').forEach(wrapper => {
        wrapper.addEventListener('click', () => {
            const imgSrc = wrapper.querySelector('img').getAttribute('src');
            overlayImg.setAttribute('src', imgSrc);
            overlay.classList.remove('d-none');
        });
    });

    closeBtn.addEventListener('click', () => {
        overlay.classList.add('d-none');
    });

    overlay.addEventListener('click', (e) => {
        if (e.target === overlay) overlay.classList.add('d-none');
    });
</script>

